/**
 * 団体名からカテゴリを推定する。確信が持てないときは null（＝現状維持）。
 */
export function classifyCategory(name: string | null, university?: string | null): Category | null {
  if (!name || !name.trim()) return null;
  const s = normalizeOrgName(name, university);

  for (const rule of RULES) {
    if (rule.keywords.some((k) => s.includes(k))) return rule.category;
  }
  return null;
}
