/**
 * 分野の手がかりにならない部分を team 名から取り除く。
 *
 * 「アメリカンフットボール部(医学部)」の「医学」で学術と誤判定したり、
 * 「国際基督教大学◯◯」の「国際」で国際交流と誤判定するのを防ぐ。
 * 学部名・大学名は所属の表記であって、活動分野を表していない。
 */
export function normalizeOrgName(name: string, university?: string | null): string {
  let s = name;

  // 大学名フィールドが分かっているならそれを優先して落とす
  if (university && university.trim()) s = s.split(university.trim()).join("");

  // 「(医学部)」「経済学部」などの所属表記
  s = s.replace(/[一-龥ぁ-んァ-ヶー]{1,6}学部/g, "");

  // 「◯◯大学」。ただし「大学祭」はイベント名なので残す
  s = s.replace(/[一-龥ぁ-んァ-ヶーA-Za-z]{2,8}大学(?!祭)/g, "");

  return s;
}

